package to.augmented.reality.android.em.recorder.util;

public class MutableTriple<T1, T2, T3>
//====================================
{
   public T1 one;
   public T2 two;
   public T3 three;

   public MutableTriple(T1 one, T2 two, T3 three)
   {
      this.one = one;
      this.two = two;
      this.three = three;
   }
}
